package day2;

public class SignOperator {

	public static void main(String[] args) {
		/*연산자의 종류
		 * 1. 단항 연산자 2. 이항연산자 3. 삼항 연산자
		 * 항 : 식을 계산하는데 필요한 수의 갯수
		 * 단항연산자? 
		 * a) 부호연산자 : -
		 * b) 증감 연산자 : ++, -- 
		 * */
		
		// 부호 연산자 예제
		int num1 = 10;
		System.out.println("-num1 = " + (-num1));
		int num2 = -20;
		System.out.println("-num2 =" + (-num2));
		
	}

}
