package Practice;

public class array2 {

	public static void main(String[] args) {
		/*5개짜리 정수 배열에 1부터 5까지 차례대로 저장 후 출력하는 예제*/
		
		
		// 5개짜리 정수배열 생성
		int arr1[]=new int[5], i;
		
		
		// 반복문으로 정수를 +1씩 올리면서 저장 + 반복마다 저장값 출력
		for(i=0; i<5; i+=1) {
			arr1[i]=i+1;
			System.out.print(arr1[i] + " ");
		} 
		
	
		
		
		
		
		
		
		
		

	}

}
